/**
 * Helper function to get optimized image URL with custom parameters
 * @param url - Base Unsplash URL
 * @param width - Desired width
 * @param quality - Image quality (1-100)
 */
export function getOptimizedImageUrl(
  url: string,
  width: number = 1920,
  quality: number = 80
): string {
  const urlObj = new URL(url)
  urlObj.searchParams.set('w', width.toString())
  urlObj.searchParams.set('q', quality.toString())
  urlObj.searchParams.set('auto', 'format')
  urlObj.searchParams.set('fit', 'crop')
  return urlObj.toString()
}

/**
 * Get a random image from a category
 */
export function getRandomImage(category: keyof typeof images): string {
  const categoryImages = images[category]
  const imageKeys = Object.keys(categoryImages)
  const randomKey = imageKeys[Math.floor(Math.random() * imageKeys.length)]
  return (categoryImages as any)[randomKey]
}

/**
 * Preload important images for better performance
 */
export function preloadImages(imageUrls: string[]) {
  if (typeof window === 'undefined') return

  imageUrls.forEach((url) => {
    const link = document.createElement('link')
    link.rel = 'preload'
    link.as = 'image'
    link.href = url
    document.head.appendChild(link)
  })
}

